public class ItemsGenerales {
    private int id;
    private String titulo;
    private String sintesis;
    private String balazo;
    private String imagen;
    private String fuente;
    private String fecha;
    private String hora;
    private int id_seccion;
    private String titulo_seccion;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getSintesis() {
        return sintesis;
    }

    public void setSintesis(String sintesis) {
        this.sintesis = sintesis;
    }

    public String getBalazo() {
        return balazo;
    }

    public void setBalazo(String balazo) {
        this.balazo = balazo;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getFuente() {
        return fuente;
    }

    public void setFuente(String fuente) {
        this.fuente = fuente;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public int getId_seccion() {
        return id_seccion;
    }

    public void setId_seccion(int id_seccion) {
        this.id_seccion = id_seccion;
    }

    public String getTitulo_seccion() {
        return titulo_seccion;
    }

    public void setTitulo_seccion(String titulo_seccion) {
        this.titulo_seccion = titulo_seccion;
    }
}
